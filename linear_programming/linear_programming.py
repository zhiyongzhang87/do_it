# linear programming (lp)
# definition:  given a set of real numbers a1, ..., an
#   and a set of variables x1, ..., xn
#   a linear function f 
#   if b in R(real number), f is a linear function
#   the equation f(x1,...,xn) = b is a linear equation
#   f() >= b and f() <= b linear inequalities
#   
# a LP problem is a problem of maximize/minimize a linear function
#   subject to a finite set of linear constraint
# function f is objective function
# a feasible solution is assignment to the variables satisfying constraints
